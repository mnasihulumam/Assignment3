// membuat array baru dengan variabel my_bucket_list
my_bucket_list = ["Lulus Kuliah", "Bekerja", "Menikah", "Bahagia"];
console.log(my_bucket_list)

// Menambahkan array di index paling belakang dengan push
my_bucket_list.push("Owner Bisnis");
console.log(my_bucket_list);

// menambahkan array di index paling depan dengan unshift
my_bucket_list.unshift("Mandiri Finansisal");
console.log(my_bucket_list);

// Menghapus item terakhir dengan pop
my_bucket_list.pop(0);
console.log(my_bucket_list);

// Menghapus item pertama dengan shift
my_bucket_list.shift();
console.log(my_bucket_list);
